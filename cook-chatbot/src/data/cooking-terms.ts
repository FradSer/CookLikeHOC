export function findCookingTerm(term: string): CookingTerm | undefined {
  return COOKING_TERMS.find(t => t.term === term || t.term.includes(term))
}

export function getAllCookingTerms(): string[] {
  return COOKING_TERMS.map(t => t.term)
}